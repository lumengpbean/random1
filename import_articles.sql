      <p>我的家在阿姨的打理下洁净如酒店，我用的是全套英国的骨瓷下午茶茶具、真丝床单、水晶红酒杯、全套的法国厨具、香氛蜡烛，我的生活如诗如画。</p>

      <p>他跟我说，让我去住他的房子。我当时觉得奇怪，我既不需要他付房贷，又不需要他付房租。好好的，为什么要搬家呢？</p>

      <p>但是经不住他长期的软磨硬泡，最终我还是去了。当时被感性蒙蔽了双眼，忽视了物质生活上的巨大落差，也吃了有生以来吃过的最大的苦。（在住酒店公寓以前，我住的是家里300多平的复式住宅）</p>

      <p>最终，他的房子在我的管理下也变得洁净如新，而我消耗了大量可以花在工作上的精力。</p>

      <p>最令人无语的是，由于我住他的房子，无论我怎么磨破嘴皮告诉周围人我条件比他好，都没有人相信。</p>

      <p>过去父亲赠予我、或者我自己购买的昂贵的配饰，都成了他财富的间接证明。如果我胆敢自证，别人就会反问我：“既然如此，你怎么会在这里呢？”我哑口无言。</p>

      <h2>2.</h2>

      <p>再后来我创业，我只租了一个很小的办公室，但是够用了。虽然寒酸，但是我认为比那些一开始把一半的资金用在装修上的初创公司要好得多了。如果你是投资人，你更愿意投哪种？你愿意为创始人的红木茶台买单吗？【微笑】</p>

      <p>这时，有一个大哥主动跟我说：我给你办公场地，还给你车，你不用花钱。</p>

      <p>我一琢磨，不对呀。我有办公场地，我也有车，我为什么要用他的？</p>

      <p>我的办公场地虽小，那是一个独立完整的“主权国家”，在这里会客，我有我的主场心理优势。他的场地再大再豪华，我去了是门客，成了一个依附别人的人，我又怎么有心力去洽谈呢？</p>

      <p>至于车就更离谱了，我雇人开我的车，自己掌握方向盘。用他的车，我成了“被接送的女人”。</p>

      <p>一个依附别人、被接送的女人还怎么在社会上立足？</p>

      <p>
        而且我寻思，他给我如此优厚待遇，也未必完全是助人为乐。他的场所巨大而人员稀疏，需不需要增加人气？我倘若去了，他会客的时候，是不是可以叫上隔壁的我？“小贾，你过来一下”。这时，无论我怎么自证复旦毕业、自有资金也没用了，我的履历越辉煌，只会显示他这个大哥越强大。
      </p>

      <p>名为提供场地，实为“驻场办公”，这样的好意我无法接受。</p>

      <p>当然，有人可能会觉得，我曲解别人的好意，或者只是单纯不合适。哈哈，见仁见智吧。</p>

      <p>又过了一段时间，随着公司合规的需要，我真的需要扩增一个场地。这时，我想到了他。他不是愿意帮我吗？我问他哪里有费用低廉、可以放在我名下的场所。既然之前他那个场所愿意“无偿”借给我，那现在我用低价买断，肯定没问题吧？</p>

      <p>果不其然，没有下文了？？？要不是我之前吃过上一个教训，差点又踩了这个坑。</p>

      <h2>3.</h2>

      <p>后来我发现这一套这么好使，我也试过反向用在别人身上。</p>

      <p>我认识一个体户，男的，自己在网上做点生意，没有办公地址。我便对他说：你到我这里来，我免费给你提供办公地址，我的车平时不用的时候可以给你开，你做你的，我做我的，我也不给你发工资。</p>

      <p>其实我心里打的算盘是，只要他来了，他是不是能帮我看个门、收个快递？他用我的车，偶尔是不是能当一下我的司机？最后我一分钱不花，得到了一个兼职人员，还对外收获了仁德慷慨的美名。</p>

      <p>对方一开始答应了。但最后又没答应。</p>

      <p>或者说他不是没答应，而是用另一种方式告诉我他不答应。</p>

      <p>他对我说：我能追你吗？</p>

      <p>我回复他删除拉黑一条龙。</p>

      <h2>4.</h2>

      <p>还有一类是饭局上，这种我早年遇到比较多。有的男的邀请你参加一个应酬，你没多想，想着这是拓展人脉的机会便去了。</p>

      <p>但你在不知情的情况下，不知道被以什么身份介绍了。</p>

      <p>后来你意识到，他们请个商务KTV的陪酒小姐，姿容如我的，要花3000到5000元。</p>

      <p>我知道很多读者读到这里，肯定在心里骂了，“臭娘们，清高什么”。</p>

      <p>没错，我不清高。首先我不评判和干涉别人的行为，成年人只要你情我愿，价值互换，都是别人的事情；其次我自己也不是什么道德标兵，如果有人给我10个亿，我一定出卖自己的灵魂。</p>

      <p>问题是，这个局里面，对方省了3000到5000元公关费用，而我花了时间、精力、化妆品摊销、名誉（无形资产）还得到了折损。</p>

      <p>这不是清高不清高的问题，这是价值交换根本就他妈不对等。</p>

      <h2>5.</h2>

      <p>同样地，我发现这一套这么好使，我也试过反向用在男人身上。</p>

      <p>我找过几个以前创业失败的老板，谈吐气度确实是招聘的普通员工不能比的，我想着用这样的人，岂不是能借他的履历添我的光？</p>

      <p>结果呢，他们在我会客的时候擅自坐到主位上，冒充合伙人。</p>

      <p>我寻思着，如果不加以制止，很有可能最后变成了我给他们发工资，对外他们拿着我的名头招摇撞骗。甚至别人还误以为我们有什么男女关系，合伙创业。</p>

      <p>我呸。删除拉黑一条龙。</p>

      <h2>6.</h2>

      <p>我还观察到，很多老板背后都有一个老板娘，管财务人事打杂。</p>

      <p>如果说这些人是因为系统已经建成，作为系统的附属品，要优于出去找工作。</p>

      <p>那还有一种我就更不能理解了：男的还在白手起家阶段，这个女的就开始出力。</p>

      <p>后来我发现这一套这么好使，我也试过反向用在几个似乎对我有点意思、又有工作能力的大公司男员工身上，结果他们无一例外都义正言辞地拒绝我。</p>

      <p>不仅拒绝我，还过问我的资金状况，仿佛我创业花的是他们的钱。我不疼，他们疼。</p>

      <p>是啊，一个女人的钱怎么胆敢不用来给家庭添砖加瓦，用来给自己赌前程？</p>

      <p>性别一调，可真不好忽悠啊。</p>

      <p>其实这样的事情无时无刻不在世界上的其他地方发生：我的女朋友用自己挣的钱给她老公的生意做现金流拆借，别人却误以为她是被供养的富太。</p>

      <p>还有一件我很纳闷的事：光是据我所知的，身边的女性手上有个几百万的比比皆是。很多小公司的净资产也就只有几百万。跑一个小型商业项目只需要50万，几百万可以跑十来次了。</p>

      <p>但是这些女性全都选择拿这笔钱作为家庭的安全垫，而不是拿这笔钱去作为起始资金。</p>

      <h2>7.</h2>

      <p>经历了这么多事件，刚开始我还怀疑人生、情感受创。现在我明白了：男性就是一种类似狼群的动物，一定要选出一个“头狼”。</p>

      <p>所以他们跟你在一起的时候，就会不自觉地争夺“头狼”的位置。</p>

      <p>成为狼群里的狼，好处就是会被“头狼”保护。但是捕猎到肉，也要“头狼”先吃，如果你胆敢先吃，就会被“头狼”揍。</p>

      <p>我不加入你的狼群。</p>

      <p>我是自己的“头狼”，如果没有人归顺我，那我就做一头孤狼。</p>

      <p>但我和别的狼群可以合作，一起捕猎。</p>

      <p>如果任何人想做我的“头狼”，那就来一场你死我活的决斗，要么你咬死我，要么我咬死你。</p>

