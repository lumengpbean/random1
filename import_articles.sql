      <p>
        灭绝师太对峨眉派弟子的严格管理——尤其是禁止与男性有情感关系的政策——往往被批评为"压抑"和"不人道"。但当我们将这一政策放入其社会背景中审视时，它的理性性就变得明显：
      </p>
      <p>
        在金庸小说的世界观中，女性武学者面临一个独特的困境：她们的身体既是力量的来源（修为），也是脆弱性的根源（生育、怀孕会削弱战斗力）。许多男性武学者在掠夺女性后遗弃责任——这在小说中被描写为常见现象。灭绝师太的政策（禁止与男性发生关系）直接回应了这一结构性威胁。
      </p>
      <p>
        更深层地，这一政策维护的是女性的经济独立性和自主权。在父权制社会中，女性一旦与男性有了关系，就容易陷入依附状态。灭绝师太通过禁止政策，强制性地保护了每一个女弟子的独立身份。从这个意义上说，她的政策是"父权制社会中的女性自保机制"。
      </p>
      <p>
        当然，这一政策的代价是高的——它压制了女性的个人感情自由。但问题是：在一个不平等的社会中，真正的自由可能是一种奢侈。灭绝师太选择了用组织纪律来换取集体的安全——这是一种"悲剧性的理性选择"，而非简单的"恶毒"。[4]
      </p>

